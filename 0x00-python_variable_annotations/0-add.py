#!/usr/bin/env python3
"""
Module: 0-add

This module provides a function for determining the addition
of two float numbers.
"""


def add(a: float, b: float) -> float:
    """Adds two float number and returns their result.
    Args:
        a (float): First float number
        b (float: Second float number

    Return:
        The sum of two float numbers.
    """
    return a + b
